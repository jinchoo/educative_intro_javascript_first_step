//Creating a Variable

// We create a variable with the let keyword.  We write let and then the variable name we want afterward.
// We give it the value we want using =, just like a line from alebra.

let number = 10

// The variable number now has the value 10.

// We Can also use the var keyword to declare a variable.  It coes essentially the same thing but behaves a little differetly, sometimes in unexpected ways.

var number1 = 10

