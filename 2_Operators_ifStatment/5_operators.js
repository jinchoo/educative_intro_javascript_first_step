//Operators: +, _, *, /, %

//We can peform math with JavaScript.  Addition, subtraction, multiplication, and division are simple.

let num1 = 10
let num2 = 5

console.log(num1 + num2) // 15
console.log(num1 - num2) // 5
console.log(num1 / num2) // 2
console.log(num1 * num2) // 50

//We can assign the result of mathematicl operations to new variables

let num3 = 10
let num4 = 5

let addition = num3 + num4
let subtraction = num3 - num4
let multiplication = num3 * num4
let division = num3 / num4

console.log(addition) // 15
console.log(subtraction) // 5
console.log(multiplication) // 50
console.log(division) // 2

//These operator follow the stadard order of operations.

